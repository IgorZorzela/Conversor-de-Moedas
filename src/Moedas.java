public class Moedas {

    private String Real;
    private String Dolar;
    private String Euro;
    private String Libras;
    private String Peso_arg;
    private String Peso_chi;
    
    public String getReal() {
        return Real;
    }
    public void setReal(String real) {
        Real = real;
    }
    public String getDolar() {
        return Dolar;
    }
    public void setDolar(String dolar) {
        Dolar = dolar;
    }
    public String getEuro() {
        return Euro;
    }
    public void setEuro(String euro) {
        Euro = euro;
    }
    public String getLibras() {
        return Libras;
    }
    public void setLibras(String libras) {
        Libras = libras;
    }
    public String getPeso_arg() {
        return Peso_arg;
    }
    public void setPeso_arg(String peso_arg) {
        Peso_arg = peso_arg;
    }
    public String getPeso_chi() {
        return Peso_chi;
    }
    public void setPeso_chi(String peso_chi) {
        Peso_chi = peso_chi;
    }
    
}
